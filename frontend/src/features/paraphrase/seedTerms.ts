// 여기에 더해 impact(이 용어가 시장·주가·투자판단에 "어떻게 작용하는지")를 레벨 공통으로 두어,
// 단순 정의를 넘어 "그래서 뭘 보면 되는지"까지 알려준다(ParaphrasePanel 의 영향 블록).
